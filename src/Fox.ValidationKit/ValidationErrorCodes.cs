//==================================================================================================
// Defines standard error codes for validation rules in FVK### format.
// Error codes are categorized by validation type for easy identification.
//==================================================================================================

namespace Fox.ValidationKit;

//==================================================================================================
/// <summary>
/// Standard error codes for built-in validation rules.
/// </summary>
//==================================================================================================
public static class ValidationErrorCodes
{
    //==============================================================================================
    // Null and Empty Validation (001-099)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// Value must not be null.
    /// </summary>
    //==============================================================================================
    public const string NotNull = "FVK001";

    //==============================================================================================
    /// <summary>
    /// String must not be null or empty.
    /// </summary>
    //==============================================================================================
    public const string NotEmpty = "FVK002";

    //==============================================================================================
    /// <summary>
    /// Collection must not be empty.
    /// </summary>
    //==============================================================================================
    public const string CollectionNotEmpty = "FVK003";

    //==============================================================================================
    // Length Validation (100-199)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// String length must be at least the minimum required length.
    /// </summary>
    //==============================================================================================
    public const string MinLength = "FVK100";

    //==============================================================================================
    /// <summary>
    /// String length must not exceed the maximum allowed length.
    /// </summary>
    //==============================================================================================
    public const string MaxLength = "FVK101";

    //==============================================================================================
    /// <summary>
    /// String length must be within the specified range.
    /// </summary>
    //==============================================================================================
    public const string Length = "FVK102";

    //==============================================================================================
    // Comparison Validation (200-299)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// Value must equal the comparison value.
    /// </summary>
    //==============================================================================================
    public const string Equal = "FVK200";

    //==============================================================================================
    /// <summary>
    /// Value must not equal the comparison value.
    /// </summary>
    //==============================================================================================
    public const string NotEqual = "FVK201";

    //==============================================================================================
    /// <summary>
    /// Value must be greater than the minimum value.
    /// </summary>
    //==============================================================================================
    public const string GreaterThan = "FVK202";

    //==============================================================================================
    /// <summary>
    /// Value must be less than the maximum value.
    /// </summary>
    //==============================================================================================
    public const string LessThan = "FVK203";

    //==============================================================================================
    /// <summary>
    /// Value must be between minimum and maximum values.
    /// </summary>
    //==============================================================================================
    public const string Between = "FVK204";

    //==============================================================================================
    // Pattern Validation (300-399)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// Value must be a valid email address.
    /// </summary>
    //==============================================================================================
    public const string EmailAddress = "FVK301";

    //==============================================================================================
    /// <summary>
    /// Value must be a valid URL.
    /// </summary>
    //==============================================================================================
    public const string Url = "FVK302";

    //==============================================================================================
    /// <summary>
    /// Value must be a valid credit card number.
    /// </summary>
    //==============================================================================================
    public const string CreditCard = "FVK303";

    //==============================================================================================
    /// <summary>
    /// Value must match the specified pattern.
    /// </summary>
    //==============================================================================================
    public const string Matches = "FVK304";

    //==============================================================================================
    // Collection Validation (400-499)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// Collection must contain at least the minimum number of items.
    /// </summary>
    //==============================================================================================
    public const string MinCount = "FVK400";

    //==============================================================================================
    /// <summary>
    /// Collection must not exceed the maximum number of items.
    /// </summary>
    //==============================================================================================
    public const string MaxCount = "FVK401";

    //==============================================================================================
    // Enum Validation (500-599)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// Value must be a defined enum value.
    /// </summary>
    //==============================================================================================
    public const string IsInEnum = "FVK500";

    //==============================================================================================
    // Custom Validation (900-999)
    //==============================================================================================

    //==============================================================================================
    /// <summary>
    /// Custom validation predicate failed.
    /// </summary>
    //==============================================================================================
    public const string Must = "FVK900";
}
